function initShop() {
  const productGrid = document.getElementById('productGrid');
  const cartItems = document.getElementById('cartItems');
  const cartEmpty = document.getElementById('cartEmpty');
  const cartTotal = document.getElementById('cartTotal');
  const clearCart = document.getElementById('clearCart');
  const orderForm = document.getElementById('orderForm');

  if (!productGrid || !cartItems || !cartEmpty || !cartTotal || !orderForm) return;

  let cart = readCart();

  const formatPrice = (price) => new Intl.NumberFormat('cs-CZ', {
    style: 'currency',
    currency: 'CZK',
    maximumFractionDigits: 0
  }).format(price);

  const saveCart = () => {
    localStorage.setItem('92gallery-cart', JSON.stringify(cart));
  };

  const getProduct = (id) => shopProducts.find((product) => product.id === id);

  const renderProducts = () => {
    productGrid.innerHTML = shopProducts.map((product) => `
      <article class="product-card">
        <div class="product-image-wrap">
          <img src="${product.image}" alt="${product.name}" loading="lazy" decoding="async">
        </div>
        <div class="product-body">
          <div class="product-meta">
            <span>${product.category}</span>
            <span>${product.stock ? 'Skladem' : 'Vyprodáno'}</span>
          </div>
          <h3>${product.name}</h3>
          <p>${product.description}</p>
          <div class="product-buy">
            <strong>${formatPrice(product.price)}</strong>
            <button class="button add-to-cart" type="button" data-product-id="${product.id}" ${product.stock ? '' : 'disabled'}>Do košíku</button>
          </div>
        </div>
      </article>
    `).join('');
  };

  const renderCart = () => {
    const rows = cart
      .map((item) => {
        const product = getProduct(item.id);
        if (!product) return '';

        return `
          <div class="cart-item">
            <img src="${product.image}" alt="${product.name}" loading="lazy" decoding="async">
            <div>
              <strong>${product.name}</strong>
              <span>${formatPrice(product.price)} / ks</span>
              <div class="cart-quantity">
                <button type="button" data-cart-action="decrease" data-product-id="${product.id}">-</button>
                <span>${item.quantity}</span>
                <button type="button" data-cart-action="increase" data-product-id="${product.id}">+</button>
              </div>
            </div>
            <button class="cart-remove" type="button" data-cart-action="remove" data-product-id="${product.id}" aria-label="Odebrat ${product.name}">×</button>
          </div>
        `;
      })
      .join('');

    const total = cart.reduce((sum, item) => {
      const product = getProduct(item.id);
      return product ? sum + product.price * item.quantity : sum;
    }, 0);

    cartItems.innerHTML = rows;
    cartEmpty.hidden = cart.length > 0;
    cartTotal.textContent = formatPrice(total);
    orderForm.querySelector('button[type="submit"]').disabled = cart.length === 0;
  };

  const addToCart = (id) => {
    const existing = cart.find((item) => item.id === id);
    if (existing) {
      existing.quantity += 1;
    } else {
      cart.push({ id, quantity: 1 });
    }
    saveCart();
    renderCart();
  };

  const updateCart = (id, action) => {
    const item = cart.find((cartItem) => cartItem.id === id);

    if (action === 'remove') {
      cart = cart.filter((cartItem) => cartItem.id !== id);
    } else if (item && action === 'increase') {
      item.quantity += 1;
    } else if (item && action === 'decrease') {
      item.quantity -= 1;
      if (item.quantity <= 0) {
        cart = cart.filter((cartItem) => cartItem.id !== id);
      }
    }

    saveCart();
    renderCart();
  };

  productGrid.addEventListener('click', (event) => {
    const button = event.target.closest('.add-to-cart');
    if (!button) return;
    addToCart(button.dataset.productId);
  });

  cartItems.addEventListener('click', (event) => {
    const button = event.target.closest('[data-cart-action]');
    if (!button) return;
    updateCart(button.dataset.productId, button.dataset.cartAction);
  });

  clearCart?.addEventListener('click', () => {
    cart = [];
    saveCart();
    renderCart();
  });

  orderForm.addEventListener('submit', (event) => {
    event.preventDefault();
    if (cart.length === 0 || !orderForm.reportValidity()) return;

    const data = new FormData(orderForm);
    const lines = cart.map((item) => {
      const product = getProduct(item.id);
      return `${item.quantity}x ${product.name} - ${formatPrice(product.price * item.quantity)}`;
    });
    const total = cart.reduce((sum, item) => {
      const product = getProduct(item.id);
      return sum + product.price * item.quantity;
    }, 0);

    const body = [
      `Jméno: ${data.get('name')}`,
      `Email: ${data.get('email')}`,
      `Telefon: ${data.get('phone') || '-'}`,
      '',
      'Objednávka:',
      ...lines,
      '',
      `Celkem: ${formatPrice(total)}`,
      '',
      `Poznámka: ${data.get('note') || '-'}`
    ].join('\n');

    window.location.href = `mailto:info@92gallery.cz?subject=${encodeURIComponent('Objednávka z 92Gallery Shop')}&body=${encodeURIComponent(body)}`;
  });

  renderProducts();
  renderCart();
}

function readCart() {
  try {
    const parsed = JSON.parse(localStorage.getItem('92gallery-cart') || '[]');
    return Array.isArray(parsed)
      ? parsed.filter((item) => item && typeof item.id === 'string' && Number.isInteger(item.quantity) && item.quantity > 0)
      : [];
  } catch {
    return [];
  }
}

