function initAchievementCounter() {
  const cards = document.querySelectorAll('.achievement-card');
  if (cards.length === 0) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting || entry.target.classList.contains('counted')) return;

      const strong = entry.target.querySelector('strong');
      if (!strong) return;

      const text = strong.textContent;
      const finalValue = text.includes('92') ? 92 : text.trim() === '6' ? 6 : 0;

      if (finalValue > 0 && window.gsap) {
        const counter = { value: 0 };
        gsap.to(counter, {
          duration: 2,
          value: finalValue,
          ease: 'power2.out',
          onUpdate: () => {
            strong.textContent = Math.ceil(counter.value) + (finalValue === 92 ? '+' : '');
          }
        });
      }

      entry.target.classList.add('counted');
    });
  }, { threshold: 0.5 });

  cards.forEach((card) => observer.observe(card));
}

function initParticleTrail() {
  if (!window.gsap || window.matchMedia('(pointer: coarse)').matches) return;

  const container = document.createElement('div');
  container.id = 'particleContainer';
  container.style.cssText = 'position: fixed; inset: 0; pointer-events: none; z-index: 9999;';
  document.body.appendChild(container);

  document.addEventListener('mousemove', (event) => {
    if (Math.random() <= 0.82) return;

    const particle = document.createElement('div');
    const size = Math.random() * 8 + 4;
    const hue = Math.random() * 60 + 200;

    particle.style.cssText = `
      position: fixed;
      left: ${event.clientX}px;
      top: ${event.clientY}px;
      width: ${size}px;
      height: ${size}px;
      background: hsla(${hue}, 100%, 60%, 0.6);
      border-radius: 50%;
      pointer-events: none;
      box-shadow: 0 0 ${size * 2}px hsla(${hue}, 100%, 70%, 0.4);
    `;

    container.appendChild(particle);

    gsap.to(particle, {
      duration: Math.random() * 1.5 + 1,
      x: (Math.random() - 0.5) * 200,
      y: (Math.random() - 0.5) * 200 - 100,
      opacity: 0,
      scale: 0,
      ease: 'power2.out',
      onComplete: () => particle.remove()
    });
  });
}
