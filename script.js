function initGalleryAnimations() {
  const elements = document.querySelectorAll('.hero, .card');
  if (elements.length > 0) {
    const observer = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
          observer.unobserve(entry.target);
        }
      });
    }, { threshold: 0.15 });

    elements.forEach((element) => observer.observe(element));
  }

  const parallaxImages = document.querySelectorAll('.card img');
  const updateParallax = () => {
    parallaxImages.forEach((img) => {
      const rect = img.getBoundingClientRect();
      const offset = (rect.top + rect.height * 0.5 - window.innerHeight * 0.5) * 0.08;
      img.style.setProperty('--img-translate', `${offset}px`);
    });
  };

  window.addEventListener('scroll', () => {
    document.body.classList.toggle('scrolled', window.scrollY > 30);
    updateParallax();
  }, { passive: true });

  window.addEventListener('resize', updateParallax);
  updateParallax();
}

function initImageOverlay() {
  const images = Array.from(document.querySelectorAll('.card img'));
  if (images.length === 0) return;

  const overlay = document.createElement('div');
  overlay.id = 'imageOverlay';
  overlay.innerHTML = `
    <div id="imageOverlayContent">
      <button id="imageOverlayPrev" class="imageOverlayNav" type="button" aria-label="Předchozí obrázek">&lsaquo;</button>
      <div id="imageOverlayFrame">
        <img src="data:image/gif;base64,R0lGODlhAQABAAAAACw=" alt="Detail obrázku">
        <div id="imageOverlayCaption"></div>
      </div>
      <button id="imageOverlayNext" class="imageOverlayNav" type="button" aria-label="Další obrázek">&rsaquo;</button>
    </div>
  `;
  document.body.appendChild(overlay);

  const overlayImage = overlay.querySelector('img');
  const overlayCaption = overlay.querySelector('#imageOverlayCaption');
  const prevButton = overlay.querySelector('#imageOverlayPrev');
  const nextButton = overlay.querySelector('#imageOverlayNext');
  let currentIndex = 0;
  let touchStartX = 0;

  localizeOverlayButtons(getSavedLocale());

  const openOverlay = (index) => {
    currentIndex = (index + images.length) % images.length;
    const img = images[currentIndex];
    const locale = getSavedLocale();
    const fallback = translations[locale]?.['overlay.imageAlt'] || 'Detail obrázku';

    overlayImage.src = img.src;
    overlayImage.alt = img.alt || fallback;
    overlayCaption.textContent = img.alt || fallback;
    overlay.classList.add('open');
  };

  const closeOverlay = () => overlay.classList.remove('open');
  const showNext = () => openOverlay(currentIndex + 1);
  const showPrev = () => openOverlay(currentIndex - 1);

  overlay.addEventListener('click', (event) => {
    if (event.target === overlay) {
      closeOverlay();
    }
  });

  prevButton.addEventListener('click', (event) => {
    event.stopPropagation();
    showPrev();
  });

  nextButton.addEventListener('click', (event) => {
    event.stopPropagation();
    showNext();
  });

  document.addEventListener('keydown', (event) => {
    if (!overlay.classList.contains('open')) return;
    if (event.key === 'Escape') closeOverlay();
    if (event.key === 'ArrowRight') showNext();
    if (event.key === 'ArrowLeft') showPrev();
  });

  overlayImage.addEventListener('touchstart', (event) => {
    touchStartX = event.changedTouches[0].clientX;
  });

  overlayImage.addEventListener('touchend', (event) => {
    const diff = event.changedTouches[0].clientX - touchStartX;
    if (Math.abs(diff) > 40) {
      diff < 0 ? showNext() : showPrev();
    }
  });

  images.forEach((img, index) => {
    img.addEventListener('click', () => openOverlay(index));
  });
}

