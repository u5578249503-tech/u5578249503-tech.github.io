function initPageTransitions() {
  document.querySelectorAll('a[href$=".html"]').forEach((link) => {
    link.addEventListener('click', (event) => {
      const href = link.getAttribute('href');
      if (!href || href.startsWith('http') || href.startsWith('//') || window.location.pathname.endsWith(href)) {
        return;
      }

      event.preventDefault();
      document.body.classList.add('page-exit');
      setTimeout(() => {
        window.location.href = href;
      }, 450);
    });
  });
}

function initThemeSwitcher() {
  const themeToggle = document.getElementById('themeToggle');
  if (!themeToggle) return;

  const currentTheme = localStorage.getItem('92gallery-theme') || 'dark';
  document.body.classList.toggle('light-theme', currentTheme === 'light');
  updateThemeToggleLabel(themeToggle, getSavedLocale());

  themeToggle.addEventListener('click', () => {
    const isLight = document.body.classList.toggle('light-theme');
    localStorage.setItem('92gallery-theme', isLight ? 'light' : 'dark');
    updateThemeToggleLabel(themeToggle, getSavedLocale());
  });
}

function updateThemeToggleLabel(button, locale = getSavedLocale()) {
  const isLight = document.body.classList.contains('light-theme');
  const key = isLight ? 'theme.dark' : 'theme.light';
  button.textContent = translations[locale]?.[key] || button.textContent;
}

function initLocalization() {
  const locale = getSavedLocale();
  translatePage(locale);

  const localeSwitcher = document.querySelector('.locale-switcher');
  const localeToggle = document.getElementById('localeToggle');
  const localeMenu = document.querySelector('.locale-menu');

  localeToggle?.addEventListener('click', () => {
    localeSwitcher?.classList.toggle('open');
  });

  localeMenu?.addEventListener('click', (event) => {
    const target = event.target.closest('.locale-option');
    if (!target) return;
    setLocale(target.dataset.locale);
    closeLocaleMenu();
  });

  document.addEventListener('click', (event) => {
    if (!localeSwitcher?.contains(event.target)) {
      closeLocaleMenu();
    }
  });
}

function closeLocaleMenu() {
  document.querySelector('.locale-switcher')?.classList.remove('open');
}

function setLocale(locale) {
  if (!translations[locale]) return;
  localStorage.setItem('92gallery-lang', locale);
  translatePage(locale);
}

function getSavedLocale() {
  const savedLocale = localStorage.getItem('92gallery-lang');
  if (savedLocale && translations[savedLocale]) return savedLocale;

  const browserLocale = navigator.language.slice(0, 2).toLowerCase();
  return translations[browserLocale] ? browserLocale : 'cs';
}

function translatePage(locale) {
  document.documentElement.lang = locale;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const value = translations[locale]?.[element.dataset.i18n];
    if (value !== undefined) {
      element.textContent = value;
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const value = translations[locale]?.[element.dataset.i18nPlaceholder];
    if (value !== undefined) {
      element.placeholder = value;
    }
  });

  const themeToggle = document.getElementById('themeToggle');
  if (themeToggle) {
    updateThemeToggleLabel(themeToggle, locale);
  }

  updateLocaleButtons(locale);
  localizeOverlayButtons(locale);
}

function updateLocaleButtons(locale) {
  const localeToggle = document.getElementById('localeToggle');
  if (localeToggle) {
    localeToggle.textContent = locale.toUpperCase();
  }

  document.querySelectorAll('.locale-option').forEach((button) => {
    button.classList.toggle('active', button.dataset.locale === locale);
  });
}

function localizeOverlayButtons(locale) {
  const prevButton = document.getElementById('imageOverlayPrev');
  const nextButton = document.getElementById('imageOverlayNext');
  prevButton?.setAttribute('aria-label', translations[locale]?.['overlay.prev'] || 'Previous image');
  nextButton?.setAttribute('aria-label', translations[locale]?.['overlay.next'] || 'Next image');
}

